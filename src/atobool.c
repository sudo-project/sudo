int
atobool(const char *str)
{
    if (strcasecmp(str, "true") == 0 || strcmp(str, "1") == 0)
	return TRUE;
    if (strcasecmp(str, "false") == 0 || strcmp(str, "0") == 0)
	return FALSE;
    return -1;
}
